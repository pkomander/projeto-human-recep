          <h2>5's no japão. </h2>
          <p>5´S origem após a segunda guerra mundial e um programa de qualidade criado no japão que visa previnir o desperdicio de materiais de produção da industria, o sistema tem como proposta reinventar a natureza humana em relação ao desperdicio e utilização das normas para criação de novas atividades de produção.</p>

          <p><b><u>Seiri</u></b> o senso de <i>utilização</i>: Tem o senso direcionado para os materiais. Ela propõe separar o necessário e o útil do desnecessário e do inútil. O descarte dos materiais que nao possuem utilidade são levados em consideração no processo, assim como a guarda do mesmos em estoque que geram despesas desnecessarias para empresa.</p>
          
          <p><b><u>Seiton</u></b> é o segundo senso do 5's: Representa o senso de <i>ordenação</i>. Ela também tem um senso direcionado para materiais e utiliza como forma principal o conseito de ordem pois facilita a localização das ferramentas, materiais e outros itens essenciais para a execução de uma determinada atividade, pois propõe um excelente sistema de comunicação visual como placas com informaçoes sobre os objetos para o facil acesso a eles. A vantagem do seiton ultrapassa a economia em relação ao tempo de busca do material. pois nos da um otimo controle de estoque em relação a compra de materiais que ja estão em estoque como forma de previnir gastos desnecessarios.</p>

          <p><b><u>Seiso</u></b> o senso direcionado a <i>limpeza</i>: tem como principio o foco a limpeza do local de trabalho e equipamentos utilizados nos processos de produção, pregando o conceito de que devem sempre estar limpos sem sinais de sujeira de qualquer espécie.</p>

          <p><b><u>Seiketsu</u></b>: visa os cuidados com a mente e corpo dos profissionais em relação ao trabalho. assim como a atenção a higiene individual para prevenção de risco a saude de um grupo. sendo necessario observar a existencia de locais perigosos com mal sinalização onde há perigo de forma iminente.</p>

          <p><b><u>Shitsuke</u></b> o senso de <i>autodisciplina</i>: diz que e necessario utilizar os sensos de forma habitual em conjunto com o comprometimento da utilização dos mesmos.</p>
