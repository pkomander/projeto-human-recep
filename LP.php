  				<h2>LADO PESSOAL </h2><br>
  				<P>A definição deliberada de um limite pode aumentar a sua eficiência no trabalho e reduzir o estresse em sua vida pessoal. Embora os avanços tecnológicos sejam úteis, eles fazem-nos constantemente acessíveis e muitas vezes, nos levam a quebrar as fronteiras entre trabalho e vida pessoal. Diante disso, foram elaboradas algumas dicas que ajudarão a manter as obrigações profissionais separadas das atividades familiares e momentos de lazer, além de deixar longe do ambiente de trabalho coisas pessoais, que podem prejudicar o seu dia a dia.</P>
          <br>
          <img src="images/media_Lp/image1.png">
          <br>
          <p>-Sintonize todas as distrações para aumentar a sua produtividade no trabalho. Isto significa evitar verificar o seu e-mail pessoal, mensagens de texto e correio de voz de casa enquanto trabalha. Essas atividades roubam tempo longe de sua produtividade e, em muitos casos, pode tomar de você horas de trabalho em casa;
          <p>-Limite o tempo que você passa online, evite navegar na Internet, verificar sites de redes sociais ou postar em fóruns de discussão relacionados a questões pessoais durante o horário de trabalho;
            </p>
          </p>
          <p>-Guarde seu tempo para conversas privadas com os colegas no horário de almoço e outros intervalos. Quanto mais tempo você dedicar ao trabalho, mais rápido terminará suas tarefas diárias;</p>
          <br>
          <img src="images/media_Lp/image2.jpeg" height="50%" width="50%">
          <br>
          <p>-Trabalhe mais rapidamente;
            <p>
          -Eficiência prática. Adapte os hábitos dos colegas e gestores em seu local de trabalho que sejam produtivos e eficazes. Isso irá ajudá-lo a realizar mais trabalho em menos tempo;
            </p>
            <p>
          -Limite seu horário de trabalho. Otimizando o tempo para cumprir suas obrigações sem cortar as horas necessárias para relaxar e passar tempo com a família e amigos. Estabeleça uma meta de deixar o cargo em um momento específico a cada dia. Agende 1 ou 2 dias obrigatórios de folga por semana. Isto é especialmente importante se você trabalha por conta própria ou em home office;
            </p>
            <p>
          -Enriqueça sua vida pessoal, desenvolvendo relacionamentos fora do ambiente de trabalho. Gaste o seu tempo pessoal com os amigos, se você tem uma boa amizade com seus colegas de trabalho, estabeleça como uma regra para discutir o trabalho apenas durante o horário de expediente;
            </p>
            <p>
          -Planeje atividades agradáveis, explorando e planejando eventos emocionantes e atividades com seus amigos e familiares.
            </p>
            <p>
          -Passe algum tempo sozinho. Exercite-se, medite e pratique hobbies que aumentam a sua sensação de relaxamento, bem-estar e prazer fora do trabalho;
            </p>
          </p>
          <img src="images/media_Lp/image3.jpeg" height="20%" width="20%"> <img src="images/media_Lp/image4.jpeg" width="20%" height="20%">
          <br>
          <p>Concentre-se apenas em sua vida profissional no horário de trabalho;</p>
          <p>Defina um limite de tempo para as comunicações de negócios em casa. Se você deve verificar e-mails e mensagens de trabalho, enquanto em casa, designe um tempo específico para isso, combine com seus colegas de trabalho para não o chamar com assuntos relacionados a negócios em seu dia de folga, mas deixe claro o que eles devem fazer durante estes dias;
          </p>
          <br>
          <img src="images/media_Lp/image5.jpeg" width="40%" height="40%">
          <p>-Deixe os pensamentos sobre o trabalho no trabalho. Quando em casa, foque em questões de família, hobbies e interesses pessoais, limite as discussões sobre questões de trabalho em casa e quando falando com amigos;</p>
          
          <p>-Peça aos seus amigos e familiares para lembrá-lo quando estiver praticando muita discussão sobre o trabalho. Além disso, quem é viciado em trabalho pode ter muitas dificuldades em se desvencilhar de alguns vícios e conversas. Comece pequeno, fale um pouco menos do trabalho, vá se desvencilhando destes vícios e a partir daí, crie uma vida mais saudável. Afinal, ninguém merece viver ao lado de quem fala de trabalho o dia inteiro.
          </p>
          <p>-Procure sempre se lembrar que dentro do ambiente de trabalho é preciso que os assuntos pessoais sejam deixados de lado, e o profissional é o que deve prevalecer; sendo assim, caso haja algum desentendimento por opiniões diferentes por exemplo, relaxe, trata-se de uma questão meramente profissional, nada diretamente ligado à pessoa, e sim à forma como ela trata determinado assunto ligado às questões naquele ambiente;
          </p>
          <p>Aprenda com as diversidades, o mundo é cheio delas. Sempre que algo ou alguém for contrário às suas opiniões, encare como uma forma de aprendizado, de crescimento. É muito mais gratificante trabalhar em um ambiente onde um respeita e aprende com as diferenças do outro.</p>
