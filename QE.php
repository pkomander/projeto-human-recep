            <br><h3>5 Pilares da Inteligência Emocional.</h3><br><br>
            <br>
            <br><p><b>1 – Conhecer as próprias emoções:</b> 
            O primeiro passo é se conhecer, analisar suas emoções e as ações que você faz em resposta aos estímulos. Essa é a chave da inteligência emocional!</p>
            <br>
            <p><b>2 – Controlar as emoções:</b>Tenha em mente que todos nós passamos por momentos estressantes na vida, ou nos sentimos ansiosos por algum motivo. Aprender a lidar com as emoções e controlá-las te colocará na direção certa conforme cada situação, e fará toda a diferença entre o equilíbrio e a disfunção.
            Você deve evitar pensar de imediato em um resultado negativo. Seja otimista, tente enxergar sempre o lado positivo das coisas e lembre-se que cada situação possui diversas saídas, basta você procurá-las.</p>
            <br>
            <br><p><b>3 – Automotivação:</b>
            Lembre-se que pensar antes de tomar as decisões lhe trará diversos benefícios e evitará o conflito com os seus pares e o arrependimento de seus atos.
            Ao saber utilizar adequadamente suas emoções você chegará aos seus objetivos.</p>
            
            <br><p><b>4 – Empatia:</b>
            Aprender a se colocar no lugar do outro, de reconhecer as emoções dos outros e entender seus comportamentos, nos torna mais sensíveis e abertos.</p>
            
            <br><p><b>5 – Saber se relacionar interpessoalmente:</b>
            Outro ponto chave para o sucesso é saber ter boas relações, guiando as emoções dos outros. Isso criará um ambiente positivo a sua volta, melhorando não só a sua qualidade de vida, mas também contagiando aqueles ao seu redor.</p>

