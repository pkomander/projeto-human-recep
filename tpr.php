  				<h2>( TPR ) TREINAMENTO DO RECEPCIONISTA</h2>
  				<br>
  				<br>
  				<p>Toda recepção de destaque preza por um rosto simpático para interagir com o público. Ainda que excelentes habilidades de comunicação por telefone e por e-mail sejam importantes para os recepcionistas, a comunicação cara a cara é a porta de entrada para um bom atendimento. Normalmente, quando uma pessoa (vendedor, empresário, membro da comunidade) entra no ambiente de trabalho, a primeira coisa que ela verá será o recepcionista — e a primeira impressão sempre é importante.</p>
  				
  				<p><img src="images/media_Tpr/image1.jpg" width="250" height="250"></p>
  				<!--- aqui vai uma imagem --->
  				
  				<p>É muito importante saber atender cada tipo de público de acordo com o perfil. Fatores como simpatia e paciência contam muito nesse momento, assim como outras habilidades que devem ser desenvolvidas para atender bem esse nicho.</p>
  				
  				<p>Diante disso, qualquer diversidade deve ser respeitada, independentemente de qualquer padrão, o recepcionista deve ser estimulado a ter empatia e adaptar o atendimento a cada pessoa.</p>
  				
  				<p>Conquistar o equilíbrio entre o bem-estar do cliente, os objetivos da empresa e as políticas do negócio sem ser rude e agindo com educação, exige paciência e jogo de cintura.</p>

				<p>Paciência é uma admirável habilidade para uma recepcionista se relacionar com tantas pessoas diferentes no dia a dia, suprindo as suas demandas e respondendo a todo o tipo de dúvida que aparece. Atrelado a isso, ainda é possível ter que atender clientes não muitos educados, nervosos ou impacientes, que podem acabar descontando suas frustrações justamente em quem está na recepção.</p>
				
				<p>Ser paciente está diretamente relacionado à educação no trato com essas pessoas, à flexibilidade nas ações que devem ser realizadas diariamente e à facilidade em contornar situações adversas, fazendo o encaminhamento correto para cada demanda recebida.</p>
				
				<p>A comunicação é um elemento básico na habilidade de um recepcionista. Falar corretamente, manter um tom de voz adequado, atender o telefone com simpatia e saber repassar informações de forma clara e objetiva.</p>
				
				<p>Papéis, recados, agenda, telefonemas, reuniões e compromissos, o atendimento na recepção vai muito além de receber os clientes, sendo necessária uma boa organização para manter o controle de documentos e informações importantes, assim, obtendo bons resultados.</p>
				<p>
				<img src="images/media_Tpr/image2.jpeg" width="280"	height="280">
				</p>
				<!--- aqui vai uma imagem --->
				
				<p>A imagem da empresa se reflete no profissional da recepção e isso é inevitável, a aparência não pode ser considerada exatamente uma das habilidades de um recepcionista, mas é um aspecto importante. Mesmo que o uniforme seja cedido, é importante tomar cuidado com o alinhamento da roupa, do cabelo, das unhas e dos sapatos.</p>
				
				<p>Ao contratar um recepcionista, a empresa espera que esse profissional seja capaz de lidar com as situações do cotidiano de maneira autônoma, sem precisa solicitar os seus superiores o tempo todo. É preciso saber responder perguntas, tomar decisões rápidas e escolher as palavras certas quando realmente não puder ajudar um cliente, tomando cuidado para não passar a impressão de que não sabe o que está acontecendo.</p>
				<img src="images/media_Tpr/image3.jpeg">
				<!--- aqui vai imagem --->
				<br>
				<br>•	Dê as boas-vindas, pergunte como estão e o que precisam naquele momento, sempre se    mostrando solícito;<br>
				<br>•	Atenda o mais rápido que puder, se você notar que alguma prioridade entrou no estabelecimento, termine o atendimento que você estiver fazendo e, logo em seguida, atenda o cliente preferencial, ou peça para algum outro funcionário realizar o atendimento prontamente;<br>
				<br>•	Alguns podem ter dificuldade para ficar em pé, caminhar ou, até mesmo, sentir algum desconforto, por isso, faz parte de um bom atendimento ser empático e servi-los o quanto antes;<br>
				<br>•	Dê atenção a tudo e demonstre boa vontade;<br>
				<br>•	Dificuldades ou ausência de fala e, principalmente, audição estão entre as deficiências mais comuns. Além disso, você pode se deparar com problemas como esquecimento, demora na resposta, entre outras situações atípicas. Nesses casos, é importante ter muita paciência, esperar o tempo da pessoa, deixando claro que você está ali para atendê-lo e faça perguntas que possam ajudá-lo a falar ou a se lembrar do que ele precisa;<br>
				<br>•	Use uma linguagem fácil e explique tudo muito bem, tendo cuidado com as palavras, falando de maneira clara e em tom adequado, reforçando, em seguida, se ele entendeu tudo. Se for o caso, escreva em um papel qualquer orientação que precise ser levada em consideração, para que ele possa se lembrar depois;<br>
				<br>•	Podem ou não apresentar limitações para escrever, ler, escutar ou se locomover. Em qualquer uma das situações, ajude-o, mesmo não sendo solicitado, disponibilize-se;<br>
				<br>•	Assentos preferenciais são obrigatórios. Se for o caso, fale pausadamente. Mas nunca grite.
				As pessoas com deficiência, devem ser consultadas sobre a melhor maneira de serem atendidas, assim você evita, possíveis constrangimentos com seus clientes.<br> 
				<br>•	Mantenha a pessoa sempre confortável e em segurança, fique sempre por perto e nunca a deixe sozinha;<br>
				<br>•	Ofereça seu antebraço para apoiar-se ao andar;<br>
				<br>•	Alerte sempre que houver portas, escadas ou veículos;<br>
				<br>•	Tente ser bem claro na sua forma de se comunicar, combine com ela um sinal para ser identificada;<br>
				<br>•	Objetividade ao explicar direções;<br>
				<br>•	Ao acompanhar dê-lhe o braço ou ombro e sempre caminhe na frente da pessoa;<br>
				<br>•	Avise-a dos possíveis obstáculos;<br>
				<br>•	Os cão-guias têm a responsabilidade de guiar seu dono, então, nunca os distraia;<br>
				<br>•	Ao desejar comunicar-se com um deficiente auditivo, chame a atenção dele, por meio de sinalização com as mãos ou tocando no braço;<br>
				<br>•	Procure acompanhar o passo da pessoa;<br>
				<br>•	Ofereça ajuda e pergunte como deve prosseguir;<br>
				<br>•	Sendo o caso, mantenha as muletas ou bengalas sempre próximas à pessoa;<br>
				<br>•	Sempre que for falar com uma pessoa cadeirante, procure ficar de frente e no mesmo nível do seu olhar;<br>
				<br>•	Peça permissão para movimentar a cadeira de rodas;<br>
				<br>•	Lembre-se, quando estiver empurrando alguém sentado em uma cadeira de rodas e parar para conversar com outra pessoa vire a cadeira de frente da conversa para que o cadeirante também possa participar da conversa também.<br>
